package FirstMonth;

public class Rainbow {

//Declare and initialize an array to hold the colors of the rainbow. Use a for-each loop to print out each color.
// red, orange, yellow, green, blue, indigo, and violet
    public static void main(String[] args) {

        String[] array = { "red", "orange", "yellow", "green", "blue", "indigo", "violet" };

        for ( String colors: array ){
            System.out.println(colors);
        }
    }

}
