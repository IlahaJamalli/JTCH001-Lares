package ThirdMonth.MentorLessons.Lesson27.Task1;

public class MovieStream {

    String title;
    String genre;


    public MovieStream(String title, String genre) {
        this.title = title;
        this.genre = genre;
    }

    public String getTitle() {
        return title;
    }

    public String getGenre() {
        return genre;
    }
}
